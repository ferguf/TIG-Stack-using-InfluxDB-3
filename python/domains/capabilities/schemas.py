# domains/capabilities/schemas.py

from pydantic import BaseModel, ConfigDict, Field
from typing import List, Optional
from uuid import UUID

class LocationCapabilitiesResponse(BaseModel):
    """
    Validates the JSON output generated by the vw_capabilities_location view.
    Used by the Streamlit frontend to populate Step 2 and Step 5 of the wizard.
    """
    location_id: UUID = Field(..., description="The unique identifier for the physical location/PoP")
    deployed_hardware: List[str] = Field(default_factory=list, description="Array of Role + Model combinations deployed at this site")
    location_supported_ports: List[str] = Field(default_factory=list, description="Aggregated port speeds available across all devices at this site")
    location_supported_services: List[str] = Field(default_factory=list, description="Aggregated service types available across all devices at this site")

    model_config = ConfigDict(from_attributes=True)

